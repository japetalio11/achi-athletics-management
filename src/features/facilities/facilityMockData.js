export function makeDocument(id, name, status = "Missing", fileName = "", required = true) {
  return {
    id,
    name,
    required,
    status,
    fileName,
    uploadedAt: fileName ? "2026-05-12" : "",
    reviewedBy: status === "Approved" || status === "Rejected" ? "Athletics Staff" : "",
    note: status === "Rejected" ? "Please upload the corrected version." : "",
  };
}

export function facilityToForm(facility) {
  if (!facility) return { ...emptyFacilityForm };
  return {
    name: facility.name,
    type: facility.type,
    location: facility.location,
    capacity: String(facility.capacity),
    status: facility.status,
    sportsText: facility.sports.join(", "),
    operatingHours: facility.operatingHours,
    contactPerson: facility.contactPerson,
    managingOffice: facility.managingOffice,
    description: facility.description,
    rulesSummary: facility.rulesSummary,
  };
}

export function mergeFacilityForm(facility, values) {
  return {
    ...facility,
    name: values.name.trim(),
    type: values.type,
    location: values.location.trim(),
    capacity: Number(values.capacity),
    status: values.status,
    sports: values.sportsText
      .split(",")
      .map((sport) => sport.trim())
      .filter(Boolean),
    operatingHours: values.operatingHours.trim(),
    contactPerson: values.contactPerson.trim(),
    managingOffice: values.managingOffice.trim(),
    description: values.description.trim(),
    rulesSummary: values.rulesSummary.trim(),
    updatedAt: todayIso(),
  };
}

