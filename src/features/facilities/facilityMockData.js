export function reservationToForm(reservation) {
  if (!reservation) return { ...emptyReservationForm };
  return {
    requesterName: reservation.requesterName,
    requesterType: reservation.requesterType,
    organization: reservation.organization,
    email: reservation.email,
    contactNumber: reservation.contactNumber,
    facilityId: reservation.facilityId,
    reservationDate: reservation.reservationDate,
    startTime: reservation.startTime,
    endTime: reservation.endTime,
    participantCount: String(reservation.participantCount),
    activityName: reservation.activityName,
    description: reservation.description,
    useType: reservation.useType,
    equipmentNeeds: reservation.equipmentNeeds,
    notes: reservation.notes,
    documentsAcknowledged: true,
  };
}

export function createReservationFromForm(values, facilities) {
  const facility = facilities.find((candidate) => candidate.id === values.facilityId);
  const templates = getApplicableDocuments(facility, values.requesterType);
  const id = `RSV-${String(Date.now()).slice(-6)}`;

  return {
    id,
    facilityId: values.facilityId,
    facilityName: facility?.name ?? "Selected Facility",
    requesterName: values.requesterName.trim(),
    requesterType: values.requesterType,
    organization: values.organization.trim(),
    email: values.email.trim(),
    contactNumber: values.contactNumber.trim(),
    reservationDate: values.reservationDate,
    startTime: values.startTime,
    endTime: values.endTime,
    participantCount: Number(values.participantCount),
    activityName: values.activityName.trim(),
    description: values.description.trim(),
    useType: values.useType,
    equipmentNeeds: values.equipmentNeeds.trim(),
    notes: values.notes.trim(),
    status: "Pending Review",
    documentStatus: templates.some((doc) => doc.required) ? "Missing" : "Submitted",
    reviewer: "",
    reviewNote: "",
    staffNote: "",
    documents: templates.map((template, index) =>
      makeDocument(`DOC-${String(Date.now()).slice(-4)}-${index + 1}`, template.name, "Missing", "", template.required),
    ),
    activityLog: [`${new Date().toLocaleDateString()} - Request submitted for ${formatDateTime(values.reservationDate, values.startTime, values.endTime)}.`],
    createdAt: todayIso(),
    updatedAt: todayIso(),
  };
}

export function getApplicableDocuments(facility, requesterType) {
  const templates = facility?.requiredDocuments ?? documentTemplates;
  return templates.filter((document) => document.appliesTo?.includes(requesterType));
}

export function todayIso() {
  return new Date().toISOString().slice(0, 10);
}
